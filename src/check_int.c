#include "../incl/push_swap.h"

int	check_int(int max, char *vec[])
{
	int	i;

	i = 0;
	while (i < max - 1)
	{
		if (!parse_int(vec[i + 1]))
			return (0);
		i++;
	}
	return (1);
}
